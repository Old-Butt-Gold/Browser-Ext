document.addEventListener('DOMContentLoaded', () => {
    const selectTags = document.querySelectorAll('select');

    selectTags.forEach((tag, id) => {
        for (let country_code in countries) {
            const option = document.createElement('option');
            option.value = country_code;
            option.textContent = countries[country_code];
            tag.appendChild(option);
        }
    });

    document.getElementById('translateBtn').addEventListener('click', () => {
        const text = document.getElementById('inputText').value;
        const translateFrom = document.getElementById('translateFrom').value;
        const translateTo = document.getElementById('translateTo').value;
        translateText(text, translateFrom, translateTo);
    });

    document.getElementById('speakBtn').addEventListener('click', () => {
        speakText(document.getElementById('outputText').innerText);
    });
});

function translateText(inputText, fromLang, toLang) {
    fetch(`https://api.mymemory.translated.net/get?q=${encodeURIComponent(inputText)}&langpair=${fromLang}|${toLang}`)
        .then(response => response.json())
        .then(data => {
            const translatedText = data.responseData ? data.responseData.translatedText : "Error: Could Not Translate!";
            document.getElementById('outputText').innerText = removeQuestionMarks(translatedText);
        })
        .catch(error => {
            console.error('Error:', error);
            document.getElementById('outputText').innerText = "Error: An error occurred while translating!";
        });
}

function removeQuestionMarks(text) {
    return text.replace(/^¿+|¿+$/g, '');
}

function speakText(text) {
    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = document.getElementById('translateTo').value;
    window.speechSynthesis.speak(utterance);
}